$('.bill_path').on("mouseover", (e)=>{
    if(bill_container.classList.contains("bill_container_active")) return false;
    hov_svg(e.target.id)
    document.querySelector(`.${e.target.id}`).classList.add('hover')
})
$('.bill_path').on("mouseout", (e)=>{
    if(bill_container.classList.contains("bill_container_active")) return false;
    normar_svg(e.target.id)
    document.querySelector(`.${e.target.id}`).classList.remove('hover')
})
$('.bill_path').on("click", (e)=>{
    if(bill_container.classList.contains("bill_container_active")) return false;
    billOpen(e.target.id)
})
$('.bill_right_row').on("mouseover", (e)=>{
    if(bill_container.classList.contains("bill_container_active")) return false;
    hov_svg(e.target.closest('.bill_right_row').classList[1])
})
$('.bill_right_row').on("mouseout", (e)=>{
    if(bill_container.classList.contains("bill_container_active")) return false;
    normar_svg(e.target.closest('.bill_right_row').classList[1])
})
$('.bill_open_p').on('click',(e)=>{
    billOpen(e.target.closest('.bill_right_row').classList[1])
})
document.querySelector('#bill_close').addEventListener('click',(e)=>{
    billClose();
})


function hov_svg(idSvg) {
    bill_body.classList.add('bill_left_text_h2_activ')
    document.getElementById(idSvg).setAttribute('d',mass.hover[idSvg]);
    bill_h2.style.color = mass.color[idSvg];
    bill_span.innerText = mass.percent[idSvg];
    bill_text.innerText = mass.title[idSvg];
}
function normar_svg(idSvg) {
    bill_body.classList.remove('bill_left_text_h2_activ')
    document.getElementById(idSvg).setAttribute('d',mass.normal[idSvg]);
}




const contendBody = $(".contendBody");
let flad_slider =true;

function billOpen(id) {
    bill_body.classList.add('bill_left_text_h2_activ')
    bill_h2.style.color = mass.color[id];
    bill_span.innerText = mass.percent[id];
    bill_text.innerText = "";
    bill_head.innerText = mass.title[id];
    if(id === "bill_AmirWallet") document.getElementById("bill_head_p").classList.add('bill_head_pActive')
    $("#bill_svgG path").css({fill:"#E4E4EC"})
    $("#"+id).css({fill: mass.color[id]});
    document.getElementById(id).setAttribute('d',mass.hover[id]);
    bill_left.classList.add("bill_to_left");
    bill_right.classList.add("bill_to_right");
    bill_container.classList.add("bill_container_active");
    $('#container_'+id).addClass("contendBodyActive");
    if(id === 'bill_investments' && flad_slider){
        flad_slider= !flad_slider;
        $('#investments_project_slider').slick({
            infinite: true,
            slidesToShow: 3,
            slidesToScroll: 3,
            prevArrow: "<img src='img/home/slider_slick_next.svg' class='prev' alt='1'>",
            nextArrow: "<img src='img/home/slider_slick_next.svg' class='next' alt='2'>",
            responsive: [
                {
                    breakpoint: 768,
                    settings: {
                        arrows: false,
                        autoplay:true,
                        autoplaySpeed:3000,
                    }
                },
                {
                    breakpoint: 480,
                    settings: {
                        autoplay:true,
                        autoplaySpeed:3000,
                        arrows: false,
                        slidesToShow: 2,
                        slidesToScroll: 2,
                    }
                },
        ]
        });
    }
}
function billClose() {
    setTimeout(()=>{
        $('.contendBodyActive').removeClass('contendBodyActive')
    },500);
    bill_head.innerText = 'Инвестиционные счета';
    bill_body.classList.remove('bill_left_text_h2_activ')
    bill_left.classList.remove("bill_to_left");
    bill_right.classList.remove("bill_to_right");
    document.getElementById("bill_head_p").classList.remove('bill_head_pActive')
    bill_container.classList.remove("bill_container_active");
    for(let i of mass.id){
        const pathSgg= document.getElementById(i);
        pathSgg.style.fill = mass.color[i];
        pathSgg.setAttribute('d', mass.normal[i]);
    }

}

